//! End-to-end tests for v0.2 persistent-naming-v2 attribute auto-population
//! during local-feature ops (fillet / chamfer) ÔÇö task 7b (#2831).
//!
//! Tests the full pipeline: parse Ôćĺ compile Ôćĺ Engine::build, then asserts on
//! `engine.topology_attribute_table()`. All tests are guarded by
//! `reify_kernel_occt::OCCT_AVAILABLE` and are skipped if OCCT is not present.
//!
//! Key signal: a non-empty `mod_history` in any table entry is EXCLUSIVE to
//! split propagation ÔÇö primitive seeding always writes `mod_history = Vec::new()`.
//! A test that produces Ôëą1 entry with non-empty `mod_history` verifies end-to-end
//! that the fillet/chamfer wiring actually ran and detected a parent-topology split
//! (e.g. an edge spawning two or more generated blend faces ÔÇö the spherical
//! corner-blend faces at the 8 vertices of a fully-filleted/chamfered cube).
//!
//! The RED signal: before step-6 wires the `ExecuteWithHistory` Fillet/Chamfer
//! arms in `handle.rs`, the kernel returns `AttributeHistory::None` for both ops,
//! so no propagation runs and `mod_history` is always empty Ôćĺ the
//! `has_split` assertion fails.

use reify_core::ModulePath;
use reify_ir::ExportFormat;

/// Run a source string through parse Ôćĺ compile Ôćĺ Engine::build and return
/// the engine. Returns `None` if OCCT is not available.
///
/// Mirrors `build_boolean_source` in `topology_attribute_boolean_e2e.rs`:
/// uses `OcctKernelHandle::spawn()` directly (not wrapped in
/// `SingleKernelHolder`) so that `extract_faces` / `extract_edges` /
/// `extract_vertices` are forwarded to OCCT rather than falling through
/// to the default-trait error stubs.
fn build_local_features_source(source: &str) -> Option<reify_eval::Engine> {
    if !reify_kernel_occt::OCCT_AVAILABLE {
        eprintln!("skipping: OCCT not available");
        return None;
    }

    // Parse
    let parsed = reify_syntax::parse(source, ModulePath::single("test_local_features_attr"));
    assert!(
        parsed.errors.is_empty(),
        "parse errors: {:?}",
        parsed.errors
    );

    // Compile
    let compiled = reify_compiler::compile(&parsed);
    let errors: Vec<_> = compiled
        .diagnostics
        .iter()
        .filter(|d| d.severity == reify_core::Severity::Error)
        .collect();
    assert!(errors.is_empty(), "compile errors: {:?}", errors);

    // Build with real OCCT kernel passed directly.
    let checker = reify_constraints::SimpleConstraintChecker;
    let mut engine = reify_eval::Engine::new(
        Box::new(checker),
        Some(Box::new(reify_kernel_occt::OcctKernelHandle::spawn())),
    );
    let result = engine.build(&compiled, ExportFormat::Step);

    // No Error diagnostics
    let build_errors: Vec<_> = result
        .diagnostics
        .iter()
        .filter(|d| d.severity == reify_core::Severity::Error)
        .collect();
    assert!(build_errors.is_empty(), "build errors: {:?}", build_errors);

    // Geometry output should be present
    let output = result
        .geometry_output
        .expect("build should produce geometry output");
    assert!(!output.is_empty(), "STEP output should be non-empty");

    Some(engine)
}

/// `fillet(box(10mm, 10mm, 10mm), 1mm)` ÔÇö all-edges fillet.
///
/// A fully filleted cube generates spherical corner-blend faces at its 8
/// vertices. The verified OCCT contract (local_feature_helper_contract.rs)
/// pins that ALL face_generated records are keyed by parent edges, so
/// each corner blend face is "generated by" a pair of adjacent edges ÔÇö
/// i.e. one edge maps to Ôëą2 generated faces Ôćĺ split detected Ôćĺ `ModEntry`
/// stamped on each result face Ôćĺ non-empty `mod_history`.
///
/// This is RED until step-6 wires the Fillet arm in `handle.rs::ExecuteWithHistory`.
#[test]
fn fillet_feeds_mod_history() {
    let source = r#"structure S {
    let r = fillet(box(10mm, 10mm, 10mm), 1mm)
}"#;
    let Some(engine) = build_local_features_source(source) else {
        return;
    };

    let table = engine.topology_attribute_table();
    assert!(
        !table.is_empty(),
        "topology attribute table must be non-empty after fillet build"
    );

    // At least one entry must have a non-empty mod_history ÔÇö the signature
    // of topology that was split by the fillet operation (e.g. an edge
    // whose generated blend faces were split across multiple children).
    let has_split = table.iter().any(|(_id, attr)| !attr.mod_history.is_empty());
    assert!(
        has_split,
        "expected Ôëą1 topology attribute entry with non-empty mod_history \
         (indicating a parent edge was split by the fillet, producing multiple \
         corner-blend face children); \
         table has {} entries, all with empty mod_history",
        table.len()
    );
}

/// `chamfer(box(10mm, 10mm, 10mm), 1mm)` ÔÇö all-edges chamfer.
///
/// Mirrors `fillet_feeds_mod_history`: a fully chamfered cube produces
/// triangular corner-blend faces at its 8 vertices, each generated by
/// Ôëą2 parent edges Ôćĺ split Ôćĺ non-empty `mod_history`.
///
/// This is RED until step-6 wires the Chamfer arm in `handle.rs::ExecuteWithHistory`.
#[test]
fn chamfer_feeds_mod_history() {
    let source = r#"structure S {
    let r = chamfer(box(10mm, 10mm, 10mm), 1mm)
}"#;
    let Some(engine) = build_local_features_source(source) else {
        return;
    };

    let table = engine.topology_attribute_table();
    assert!(
        !table.is_empty(),
        "topology attribute table must be non-empty after chamfer build"
    );

    let has_split = table.iter().any(|(_id, attr)| !attr.mod_history.is_empty());
    assert!(
        has_split,
        "expected Ôëą1 topology attribute entry with non-empty mod_history \
         (indicating a parent edge was split by the chamfer, producing multiple \
         corner-blend face children); \
         table has {} entries, all with empty mod_history",
        table.len()
    );
}
